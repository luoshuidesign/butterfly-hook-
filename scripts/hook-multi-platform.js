#!/usr/bin/env node
/**
 * 🪝 HOOK MULTI-PLATFORM — 多平台代码生成
 *
 * 缺1补全: SwiftUI · Jetpack Compose · Flutter · 微信小程序
 * 借: design-extract 的多平台思路
 * 化: Hook独有 — 从同一份DNA生成所有平台代码
 */

const fs = require('fs');
const path = require('path');

function tokensToSwiftUI(tokens) {
  const c = tokens.colors || {};
  const t = tokens.typography || {};
  return `// 🪝 Hook Generated · SwiftUI Theme
import SwiftUI

extension Color {
    static let surface = Color(hex: "${c['color-surface-primary'] || '#ffffff'}")
    static let surfaceCard = Color(hex: "${c['color-surface-card'] || '#f5f5f5'}")
    static let textPrimary = Color(hex: "${c['color-text-primary'] || '#000000'}")
    static let accent = Color(hex: "${c['color-accent'] || '#0066cc'}")
}

struct HookTheme {
    static let displayFont = Font.custom("${t['font-family-display'] || 'system'}", size: ${parseFloat(t['font-size-display']) || 34})
    static let bodyFont = Font.custom("${t['font-family-body'] || 'system'}", size: ${parseFloat(t['font-size-body']) || 16})
    static let cornerRadius: CGFloat = ${parseFloat(tokens.radius?.['radius-md']) || 8}
}

// Generated by Hook · ${new Date().toISOString().slice(0,10)}`;
}

function tokensToCompose(tokens) {
  const c = tokens.colors || {};
  return `// 🪝 Hook Generated · Jetpack Compose Theme
package com.hook.theme

import androidx.compose.ui.graphics.Color
import androidx.compose.ui.unit.dp
import androidx.compose.ui.text.TextStyle
import androidx.compose.ui.unit.sp

val Surface = Color(${hexToCompose(c['color-surface-primary'] || '#ffffff')})
val SurfaceCard = Color(${hexToCompose(c['color-surface-card'] || '#f5f5f5')})
val TextPrimary = Color(${hexToCompose(c['color-text-primary'] || '#000000')})
val Accent = Color(${hexToCompose(c['color-accent'] || '#0066cc')})

object HookTheme {
    val cornerRadius = ${parseFloat(tokens.radius?.['radius-md']) || 8}.dp
}

// Generated by Hook · ${new Date().toISOString().slice(0,10)}`;
}

function tokensToFlutter(tokens) {
  const c = tokens.colors || {};
  return `// 🪝 Hook Generated · Flutter Theme
import 'package:flutter/material.dart';

class HookTheme {
  static const surface = Color(${hexToFlutter(c['color-surface-primary'] || '#ffffff')});
  static const surfaceCard = Color(${hexToFlutter(c['color-surface-card'] || '#f5f5f5')});
  static const textPrimary = Color(${hexToFlutter(c['color-text-primary'] || '#000000')});
  static const accent = Color(${hexToFlutter(c['color-accent'] || '#0066cc')});

  static ThemeData get light => ThemeData(
    scaffoldBackgroundColor: surface,
    cardTheme: CardTheme(
      color: surfaceCard,
      shape: RoundedRectangleBorder(
        borderRadius: BorderRadius.circular(${parseFloat(tokens.radius?.['radius-md']) || 8}),
      ),
    ),
    textTheme: TextTheme(
      headlineLarge: TextStyle(fontSize: ${parseFloat(tokens.typography?.['font-size-display']) || 34}),
      bodyLarge: TextStyle(fontSize: ${parseFloat(tokens.typography?.['font-size-body']) || 16}),
    ),
  );
}

// Generated by Hook · ${new Date().toISOString().slice(0,10)}`;
}

function tokensToWechatMini(tokens) {
  const c = tokens.colors || {};
  return `/* 🪝 Hook Generated · 微信小程序 Theme */
/* pages/hook-theme.wxss */

page {
  --color-surface: ${c['color-surface-primary'] || '#ffffff'};
  --color-surface-card: ${c['color-surface-card'] || '#f5f5f5'};
  --color-text-primary: ${c['color-text-primary'] || '#000000'};
  --color-accent: ${c['color-accent'] || '#0066cc'};
  --font-display: ${tokens.typography?.['font-size-display'] || '34px'};
  --font-body: ${tokens.typography?.['font-size-body'] || '16px'};
  --radius-md: ${tokens.radius?.['radius-md'] || '8px'};
}

/* Generated by Hook · ${new Date().toISOString().slice(0,10)} */`;
}

function hexToCompose(hex) { const h=hex.replace('#',''); return `0xFF${h}`; }
function hexToFlutter(hex) { const h=hex.replace('#',''); return `0xFF${h}`; }

// CLI
const cmd = process.argv[2];
const arg = process.argv[3];

if (cmd === 'all') {
  const input = fs.readFileSync(0, 'utf8');
  const tokens = JSON.parse(input).tokens || JSON.parse(input).designSystem || JSON.parse(input);
  console.log('=== SwiftUI ===\n' + tokensToSwiftUI(tokens));
  console.log('\n=== Jetpack Compose ===\n' + tokensToCompose(tokens));
  console.log('\n=== Flutter ===\n' + tokensToFlutter(tokens));
  console.log('\n=== 微信小程序 ===\n' + tokensToWechatMini(tokens));
} else {
  console.log('🪝 hook-multi-platform\n  all < tokens.json  — 从stdin读取tokens·生成全部平台代码');
}
